// Service worker for PWA installability, Web Push, and offline caching.
//
// Caching strategy (conservative, online always wins):
//  - Next static assets (/_next/static, content-hashed): cache-first.
//  - Navigations (HTML): network-first, falling back to cache then "/" offline.
//  - Everything else (APIs, cross-origin TMDB/Supabase): pass-through.

const VERSION = "v5";
const CACHE = `mylist-${VERSION}`;
// Dedicated static page precached at install; "/" stays cached too as a
// second-chance fallback for navigations that were cached earlier.
const OFFLINE_FALLBACK = "/offline.html";

self.addEventListener("install", (event) => {
  // Seed the offline fallbacks; ignore failure so install never blocks on it.
  event.waitUntil(
    caches
      .open(CACHE)
      .then((cache) => cache.addAll([OFFLINE_FALLBACK, "/"]))
      .catch(() => undefined),
  );
  self.skipWaiting();
});

self.addEventListener("activate", (event) => {
  event.waitUntil(
    (async () => {
      const keys = await caches.keys();
      await Promise.all(keys.filter((k) => k !== CACHE).map((k) => caches.delete(k)));
      await self.clients.claim();
    })(),
  );
});

async function cacheFirst(request) {
  const cached = await caches.match(request);
  if (cached) return cached;
  const response = await fetch(request);
  if (response.ok) {
    const cache = await caches.open(CACHE);
    cache.put(request, response.clone());
  }
  return response;
}

async function networkFirst(request) {
  try {
    const response = await fetch(request);
    if (response.ok) {
      const cache = await caches.open(CACHE);
      cache.put(request, response.clone());
    }
    return response;
  } catch (error) {
    const cached = await caches.match(request);
    if (cached) return cached;
    const fallback = await caches.match(OFFLINE_FALLBACK);
    if (fallback) return fallback;
    throw error;
  }
}

self.addEventListener("fetch", (event) => {
  const { request } = event;
  if (request.method !== "GET") return;

  const url = new URL(request.url);
  // Only handle our own origin; leave TMDB/Supabase/etc. to the browser.
  if (url.origin !== self.location.origin) return;

  if (url.pathname.startsWith("/_next/static/")) {
    event.respondWith(cacheFirst(request));
    return;
  }

  if (request.mode === "navigate") {
    event.respondWith(networkFirst(request));
  }
});

// Web Push: the server sends a JSON payload shaped like
// { title, body, url?, icon?, tag? }. We surface it as a system notification
// and remember the target URL on the notification itself so the click handler
// can route back into the app.
self.addEventListener("push", (event) => {
  if (!event.data) return;

  let payload;
  try {
    payload = event.data.json();
  } catch {
    payload = { title: "MyList", body: event.data.text() };
  }

  const title = payload.title || "MyList";
  const options = {
    body: payload.body || "",
    icon: payload.icon || "/iconomylist.png",
    // `badge` is the tiny status-bar icon. Android masks it to a monochrome
    // silhouette using ONLY the alpha channel, so a full-colour logo turns into
    // a white blob. badge.png is a purpose-built white-on-transparent glyph
    // (generated by scripts/generate-badge.mjs).
    badge: "/badge.png",
    tag: payload.tag,
    data: { url: payload.url || "/library" },
  };

  event.waitUntil(self.registration.showNotification(title, options));
});

// When the user taps the notification: focus an existing tab if one is open
// (and navigate it to the target URL), otherwise open a fresh one. This keeps
// PWA users from ending up with a stack of duplicate windows.
self.addEventListener("notificationclick", (event) => {
  event.notification.close();
  const targetUrl = (event.notification.data && event.notification.data.url) || "/library";

  event.waitUntil(
    (async () => {
      const allClients = await self.clients.matchAll({
        type: "window",
        includeUncontrolled: true,
      });
      for (const client of allClients) {
        if ("focus" in client) {
          await client.focus();
          if ("navigate" in client) {
            try {
              await client.navigate(targetUrl);
            } catch {
              // Some browsers reject cross-origin navigate; just focus then.
            }
          }
          return;
        }
      }
      if (self.clients.openWindow) {
        await self.clients.openWindow(targetUrl);
      }
    })(),
  );
});
