// Pre-resolves the cover image for every curated franchise (the poster of its
// first chronological entry) and writes them to
// src/features/library-v2/curated-franchise-covers.ts, so the /watch-order
// index renders instantly without hitting TMDB/Jikan at request time (42 anime
// would blow Jikan's rate limit on every cold load).
//
// Re-run with `node scripts/resolve-franchise-covers.mjs` whenever the curated
// list changes. Reads TMDB_API_KEY from .env.local; anime needs no key (Jikan
// is public). Sequential with a 700 ms gap + retry on 429/5xx to stay well
// under Jikan's 3 req/s.

import { readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const here = dirname(fileURLToPath(import.meta.url));
const root = join(here, "..");

// --- TMDB key from .env.local ------------------------------------------------
function readEnv(name) {
  const txt = readFileSync(join(root, ".env.local"), "utf8");
  for (const line of txt.split(/\r?\n/)) {
    const m = line.match(/^\s*([A-Z0-9_]+)\s*=\s*(.*)$/);
    if (m && m[1] === name) {
      let v = m[2].trim();
      if ((v.startsWith('"') && v.endsWith('"')) || (v.startsWith("'") && v.endsWith("'"))) {
        v = v.slice(1, -1);
      }
      return v;
    }
  }
  return undefined;
}

const TMDB_API_KEY = readEnv("TMDB_API_KEY");
if (!TMDB_API_KEY) {
  console.error("Missing TMDB_API_KEY in .env.local");
  process.exit(1);
}

// --- Parse curated franchises: id, category, first source id -----------------
// The file is regular: each franchise is `id: "..."` then `category: "..."`
// then `chronological: movies(FIRST,...)` / `animes(FIRST,...)`. Lazy matching
// grabs the first category + first id after each franchise id.
const src = readFileSync(join(root, "src/features/library-v2/curated-franchises.ts"), "utf8");
const re = /id:\s*"([^"]+)"[\s\S]*?category:\s*"([^"]+)"[\s\S]*?(movies|animes)\(\s*(\d+)/g;
const franchises = [];
let m;
while ((m = re.exec(src)) !== null) {
  franchises.push({ id: m[1], category: m[2], firstId: m[4] });
}
console.log(`Parsed ${franchises.length} franchises`);
if (franchises.length < 40) {
  console.error("Parse looks wrong (expected ~44), aborting");
  process.exit(1);
}

// --- Helpers -----------------------------------------------------------------
const sleep = (ms) => new Promise((r) => setTimeout(r, ms));

function normalizeMal(url) {
  if (!url) return null;
  return url.replace(/^https?:\/\/myanimelist\.net\//, "https://cdn.myanimelist.net/");
}

async function fetchJson(url) {
  for (let attempt = 0; attempt < 4; attempt++) {
    const res = await fetch(url);
    if (res.ok) return res.json();
    if (res.status === 429 || res.status >= 500) {
      await sleep(1500 * (attempt + 1));
      continue;
    }
    throw new Error(`${res.status} ${res.statusText}`);
  }
  throw new Error("retries exhausted");
}

async function tmdbCover(id) {
  const url = `https://api.themoviedb.org/3/movie/${id}?api_key=${TMDB_API_KEY}&language=es-ES`;
  const data = await fetchJson(url);
  return data.poster_path ? `https://image.tmdb.org/t/p/w342${data.poster_path}` : null;
}

async function jikanCover(id) {
  const data = await fetchJson(`https://api.jikan.moe/v4/anime/${id}`);
  const img = data?.data?.images;
  const raw =
    img?.webp?.large_image_url ??
    img?.jpg?.large_image_url ??
    img?.webp?.image_url ??
    img?.jpg?.image_url ??
    null;
  return normalizeMal(raw);
}

// --- Resolve sequentially ----------------------------------------------------
const covers = {};
for (const f of franchises) {
  try {
    const url = f.category === "movie" ? await tmdbCover(f.firstId) : await jikanCover(f.firstId);
    if (url) {
      covers[f.id] = url;
      console.log(`OK  ${f.id} -> ${url}`);
    } else {
      console.warn(`--  ${f.id}: no poster`);
    }
  } catch (e) {
    console.warn(`ERR ${f.id}: ${e.message}`);
  }
  await sleep(700);
}

// --- Write the generated module ----------------------------------------------
const entries = Object.entries(covers)
  .map(([k, v]) => `  ${JSON.stringify(k)}: ${JSON.stringify(v)},`)
  .join("\n");

const out = `// AUTO-GENERATED by scripts/resolve-franchise-covers.mjs — do not edit by hand.
// Maps each curated franchise id to its cover image (the poster of its first
// chronological entry), pre-resolved so the watch-order index renders instantly
// without hitting TMDB/Jikan per request. Regenerate when franchises change.

export const FRANCHISE_COVERS: Record<string, string> = {
${entries}
};
`;

const dest = join(root, "src/features/library-v2/curated-franchise-covers.ts");
writeFileSync(dest, out);
console.log(`\nWrote ${Object.keys(covers).length}/${franchises.length} covers to ${dest}`);
